var senderscore = function(ipv4, callback) {
	//test ip is valid or not
	if (!ipv4_regex.test(ipv4)) {
		return callback(new Error("INVALID IPV4"));
	}

	var options = {
		method: 'GET',
		url: 'http://www.reputationauthority.org/lookup.php',
		qs: {
			ip: ipv4,
			ipvalid: '',
			'Submit.x': '29',
			'Submit.y': '8',
			Submit: 'Search'
		}
	};

	request(options, function(error, response, body) {
		if (error) {
			callback(error);
		} else if (response.statusCode == 200) {
			var $ = cheerio.load(body);
			var result = {};
			var keys = [];
			$('.bsnDataRow1').each(function(i, elem) {
				keys[i] = $(this).text();
			});
			var values = [];
			$('.bsnDataRow2').each(function(i, elem) {
				values[i] = $(this).text();
				result[keys[i].trim().toLowerCase().replace(/ /g, '_')] = values[i].trim();
			});
			callback(null, result);
		} else {
			callback(new Error(response.statusMessage));
		}
	});

}

module.exports = senderscore;